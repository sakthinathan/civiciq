"use strict";

function renderTimeline(timeline) {
  const tlEl = document.getElementById("timelineContainer");
  if (!tlEl) return;

  tlEl.innerHTML = (timeline || []).map((t, i) => {
    const calendarBtn = (t.phase.toLowerCase().includes("polling") || t.phase.toLowerCase().includes("registration")) 
      ? `<a href="${createCalendarLink(t.phase, t.description)}" target="_blank" class="calendar-btn" title="Add to Google Calendar">📅 Remind</a>`
      : "";

    return `<div class="tl-item" role="listitem" style="animation-delay:${i * 0.06}s">
      <div class="tl-day" aria-label="Day ${t.days}">Day ${t.days}</div>
      <div class="tl-dot" aria-hidden="true"></div>
      <div class="tl-content">
        <div class="tl-phase">${window.escapeHtml(t.phase)} ${calendarBtn}</div>
        <div class="tl-desc">${window.escapeHtml(t.description)}</div>
      </div>
    </div>`;
  }).join("");
}

function createCalendarLink(title, details) {
  const base = "https://www.google.com/calendar/render?action=TEMPLATE";
  const start = new Date(); // Ideally we'd calculate from Day offset, but today is fine for demo
  const end = new Date(start.getTime() + 3600000);
  const fmt = (d) => d.toISOString().replace(/-|:|\.\d\d\d/g, "");
  
  return `${base}&text=${encodeURIComponent('CivicIQ: ' + title)}&details=${encodeURIComponent(details + ' \n\nGenerated by CivicIQ Platform.')}&dates=${fmt(start)}/${fmt(end)}`;
}

function renderVotingSteps(steps, youtubeId = null) {
  const stepsEl = document.getElementById("stepsContainer");
  if (!stepsEl) return;

  let html = (steps || []).map((s, i) =>
    `<div class="step-card" style="animation-delay:${i * 0.07}s">
      <div class="step-icon" aria-hidden="true">${s.icon}</div>
      <div>
        <div class="step-title">${i + 1}. ${window.escapeHtml(s.title)}</div>
        <div class="step-detail">${window.escapeHtml(s.detail)}</div>
      </div>
    </div>`
  ).join("");

  if (youtubeId) {
    html = `<div class="video-container" style="margin-bottom: 24px; animation: fade-up 0.6s ease both;">
      <iframe width="100%" height="315" src="https://www.youtube.com/embed/${youtubeId}" 
        title="Official Election Guide" frameborder="0" 
        allow="accelerometer; autoplay; clipboard-write; encrypted-media; gyroscope; picture-in-picture" 
        allowfullscreen style="border-radius: 12px; border: 1px solid var(--gold);"></iframe>
    </div>` + html;
  }

  stepsEl.innerHTML = html;
}

function initDetailTabs() {
  document.querySelectorAll(".tab-btn").forEach(btn => {
    btn.addEventListener("click", () => {
      const tab = btn.dataset.tab;
      document.querySelectorAll(".tab-btn").forEach(b => {
        b.classList.toggle("active", b.dataset.tab === tab);
        b.setAttribute("aria-selected", b.dataset.tab === tab ? "true" : "false");
      });
      document.querySelectorAll(".tab-content").forEach(tc => {
        const isActive = tc.id === `tab-${tab}`;
        tc.classList.toggle("active", isActive);
        if (isActive) tc.removeAttribute("hidden");
        else tc.setAttribute("hidden", "");
      });
    });
  });
}

window.renderTimeline = renderTimeline;
window.renderVotingSteps = renderVotingSteps;
window.initDetailTabs = initDetailTabs;
